#include <iostream>
using namespace std;

int main(){
    // '<<' Insertion Operator
    // '>>' Extraction Operator
    int num1, num2;
    cout << "Enter The Value Of No. 1 \n";
    cin >> num1;
    cout << "Enter The Value Of No. 2 \n";
    cin >> num2;
    cout << "The sum Of two number is " << num1 + num2;
    return 0;
}